package com.xworkz.microwave;

public class Microwave {
	
	private String brand;
	private String size;
	public Microwave(String brand, String size) {
		super();
		this.brand = brand;
		this.size = size;
	}
	public String getBrand() {
		return brand;
	}
	public String getSize() {
		return size;
	}
	
	
	

}
